// Write a switch statement that 
// console logs witty statements for each
// day of the standard work week: Monday, Tuesday,
// Wednesday, Thursday, Friday
var day = "Thursday";

switch (day) {
	case "Monday":
	console.log("Monday is Monday");
	break;
	case "Tuesday":
	console.log("Tuesday is Tuesday");
	break;
	case "Wednesdy":
	console.log("Wednesday is Wednesday");
	break;
	case "Thursday":
	console.log("Thursday is Thursday");
	break;
	case "Friday":
	console.log("Friday is Friday");
};

